# Assign values to two variables as specified in the instructions
# number1 stores the first number for arithmetic operations
number1 = 10
# number2 stores the second number for arithmetic operations
number2 = 5

# Perform addition: add number1 and number2 together
# sum_result stores the result of the addition operation
sum_result = number1 + number2

# Perform subtraction: subtract number2 from number1
# difference_result stores the result of the subtraction operation
difference_result = number1 - number2

# Perform multiplication: multiply number1 by number2
# product_result stores the result of the multiplication operation
product_result = number1 * number2

# Print the results in the specified format using f-strings
# f-strings allow us to embed variables directly in the string
print(f"Addition of {number1} and {number2} is {sum_result}")
print(f"Subtraction of {number1} and {number2} is {difference_result}")
print(f"Multiplication of {number1} and {number2} is {product_result}")