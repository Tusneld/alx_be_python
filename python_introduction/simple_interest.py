# Define variables with the specified values for simple interest calculation
# principal represents the initial amount of money invested ($1000)
principal = 1000
# rate represents the annual interest rate as a decimal (5% = 0.05)
rate = 0.05
# time represents the number of years the money is invested
time = 3

# Calculate simple interest using the formula: I = P * R * T
# Where I is interest, P is principal, R is rate, T is time
# interest stores the calculated simple interest amount
interest = principal * rate * time

# Print the calculated interest
# The result will be displayed as a float (decimal number)
print(f"The simple interest is: {interest}")